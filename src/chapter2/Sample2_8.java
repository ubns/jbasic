package chapter2;

public class Sample2_8 {
	public static void main(String[] args) {
		int x = 300;
		if (x == 100) {
			System.out.println("xの値は100です。");
		} else if (x == 200) {
			System.out.println("xの値は200です。");
		} else if (x == 300) {
			System.out.println("xの値は300です。");
		} else {
			System.out.println("エラー");
		}
	}
}
